package Fundamentals;
public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a > b); // false
        System.out.println(a < b); // true
        System.out.println(a >= b); // false
        System.out.println(a <= b); // true
        System.out.println(a == b); // false
        System.out.println(a != b); // true

        int x = 6;
        int y = 5;

        boolean result = x > y;
        System.out.println(result);

        double u = 8.8;
        double w = 9.8;

        boolean result2 = u >= w;
        System.out.println(result2);

        
    }
}
